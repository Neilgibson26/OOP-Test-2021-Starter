public class Note {
    private int duaration;
    private char note;

    
    public Note(int duaration, char note) {
        this.duaration = duaration;
        this.note = note;
    }
    public int getDuaration() {
        return duaration;
    }
    public void setDuaration(int duaration) {
        this.duaration = duaration;
    }
    public char getNote() {
        return note;
    }
    public void setNote(char note) {
        this.note = note;
    }
    
    
}
